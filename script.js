function renderEvidence(evidence) {
  const hasPhotos = evidence.images && evidence.images.length > 0;
  const hasVideo = !!evidence.video;
  const hasPdf = !!evidence.pdf;

  const tabs = [
    { id: "photos", label: "Photos", icon: "images", available: hasPhotos },
    { id: "video", label: "Video", icon: "video", available: hasVideo },
    { id: "pdf", label: "PDF", icon: "file-text", available: hasPdf },
  ];

  evidenceTabs.innerHTML = tabs.map((t, i) => `
    <button class="evidence-tab ${i === 0 ? "active" : ""}" type="button" data-tab="${t.id}">
      <i data-lucide="${t.icon}"></i> ${t.label}
    </button>
  `).join("");

  // Panel: Photos / carousel
  if (hasPhotos) {
    carouselRoot.innerHTML = buildCarouselHTML(evidence.images);
    initCarousel(evidence.images);
  } else {
    carouselRoot.innerHTML = `<div class="evidence-empty">No photos yet. Add image files to your week's assets folder and list them in "evidence.images".</div>`;
  }

  // Panel: Video
  if (hasVideo) {
    panelVideo.innerHTML = `
      <div class="evidence-video">
        <video controls ${evidence.video.poster ? `poster="${evidence.video.poster}"` : ""}>
          <source src="${evidence.video.src}">
        </video>
      </div>
      <a class="evidence-download" href="${evidence.video.src}" download>
        <i data-lucide="download"></i> Download video
      </a>
    `;
  } else {
    panelVideo.innerHTML = `<div class="evidence-empty">No video yet. Add a video file to your week's assets folder and set "evidence.video".</div>`;
  }

  // Panel: PDF
  if (hasPdf) {
    panelPdf.innerHTML = `
      <div class="evidence-pdf">
        <iframe src="${evidence.pdf.src}" title="PDF evidence"></iframe>
      </div>
      <a class="evidence-download" href="${evidence.pdf.src}" download="${evidence.pdf.name || "evidence.pdf"}">
        <i data-lucide="download"></i> Download PDF
      </a>
    `;
  } else {
    panelPdf.innerHTML = `<div class="evidence-empty">No PDF yet. Add a PDF file to your week's assets folder and set "evidence.pdf".</div>`;
  }

  // Tab switching
  const tabButtons = evidenceTabs.querySelectorAll(".evidence-tab");
  const panels = { photos: panelPhotos, video: panelVideo, pdf: panelPdf };
  tabButtons.forEach(btn => {
    btn.addEventListener("click", () => {
      tabButtons.forEach(b => b.classList.remove("active"));
      Object.values(panels).forEach(p => p.classList.remove("active"));
      btn.classList.add("active");
      panels[btn.dataset.tab].classList.add("active");
    });
  });

  lucide.createIcons();
}

function buildCarouselHTML(images) {
  return `
    <div class="carousel-view">
      ${images.map((img, i) => `<img src="${img.src}" alt="${img.caption || ""}" class="${i === 0 ? "active" : ""}" data-slide="${i}">`).join("")}
      ${images.length > 1 ? `
        <button class="carousel-btn prev" type="button" aria-label="Anterior"><i data-lucide="chevron-left"></i></button>
        <button class="carousel-btn next" type="button" aria-label="Siguiente"><i data-lucide="chevron-right"></i></button>
      ` : ""}
    </div>
    ${images.length > 1 ? `
      <div class="carousel-dots">
        ${images.map((_, i) => `<button data-dot="${i}" class="${i === 0 ? "active" : ""}"></button>`).join("")}
      </div>
    ` : ""}
    <a class="evidence-download" href="${images[0].src}" download>
      <i data-lucide="download"></i> Download current photo
    </a>
  `;
}

function initCarousel(images) {
  let current = 0;
  const slides = carouselRoot.querySelectorAll("[data-slide]");
  const dots = carouselRoot.querySelectorAll("[data-dot]");
  const prevBtn = carouselRoot.querySelector(".carousel-btn.prev");
  const nextBtn = carouselRoot.querySelector(".carousel-btn.next");
  const downloadLink = carouselRoot.querySelector(".evidence-download");

  function show(i) {
    current = (i + images.length) % images.length;
    slides.forEach(s => s.classList.toggle("active", parseInt(s.dataset.slide) === current));
    dots.forEach(d => d.classList.toggle("active", parseInt(d.dataset.dot) === current));
    if (downloadLink) downloadLink.href = images[current].src;
  }

  if (prevBtn) prevBtn.addEventListener("click", () => show(current - 1));
  if (nextBtn) nextBtn.addEventListener("click", () => show(current + 1));
  dots.forEach(d => d.addEventListener("click", () => show(parseInt(d.dataset.dot))));
}

